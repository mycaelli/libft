#include "libft.h"

char *ft_substr(char const *s, unsigned int start, size_t len)
{
    char *sub;
    unsigned int i;

    i = 0;
    sub = NULL;
    sub = malloc((len + 1) * sizeof(char));
    if (!sub)
        return (NULL);
    while (i < len && s[start + i])
    {
        sub[i] = s[start + i];
        i++;
    }
    sub[i] = '\0';
    return (sub);
}
